def area(a, b): 
    ''' Принимает две стороны прямоугольника и находит его площадь
            area(7,19)
            area(10,20)
            area(20,40)

            >> 133 200 800'''
    return a * b 

def perimeter(a, b): 
    ''' Принимает две стороны прямоугольника и находит его периметр
            perimeter(3,5)
            perimeter(5,10)
            perimeter(10,20)
            
            >> 16 30 60'''
    return (a + b)*2 